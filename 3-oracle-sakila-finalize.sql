-- --- Row-count tripwire (build fails here if conversion drifted) ------------
DECLARE
  PROCEDURE assert_count(p_table VARCHAR2, p_expected NUMBER) IS
    v_got NUMBER;
  BEGIN
    EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM ' || p_table INTO v_got;
    IF v_got <> p_expected THEN
      RAISE_APPLICATION_ERROR(
        -20001,
        'sakila row-count mismatch on ' || p_table ||
        ': expected ' || p_expected || ', got ' || v_got);
    END IF;
  END;
BEGIN
  assert_count('actor',     200);
  assert_count('address',   603);
  assert_count('category',  16);
  assert_count('city',      600);
  assert_count('country',   109);
  assert_count('customer',  599);
  assert_count('film',      1000);
  assert_count('film_actor', 5462);
  assert_count('film_category', 1000);
  assert_count('inventory', 4581);
  assert_count('language',  6);
  assert_count('payment',   16049);
  assert_count('rental',    16044);
  assert_count('staff',     2);
  assert_count('store',     2);
END;
