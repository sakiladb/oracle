-- --- Realign identity columns -----------------------------------------------
-- Explicit-id INSERTs do not advance the underlying identity sequence, so
-- without this any subsequent INSERT that omits the id would collide with
-- existing rows. RESTART STARTS WITH MAX(id)+1 fixes that.
DECLARE
  PROCEDURE realign(p_table VARCHAR2, p_id_col VARCHAR2) IS
    v_next NUMBER;
  BEGIN
    EXECUTE IMMEDIATE
      'SELECT NVL(MAX(' || p_id_col || '),0)+1 FROM ' || p_table
      INTO v_next;
    EXECUTE IMMEDIATE
      'ALTER TABLE ' || p_table ||
      ' MODIFY ' || p_id_col || ' GENERATED BY DEFAULT AS IDENTITY (START WITH ' ||
      v_next || ')';
  END;
BEGIN
  realign('actor',     'actor_id');
  realign('address',   'address_id');
  realign('category',  'category_id');
  realign('city',      'city_id');
  realign('country',   'country_id');
  realign('customer',  'customer_id');
  realign('film',      'film_id');
  realign('inventory', 'inventory_id');
  realign('language',  'language_id');
  realign('payment',   'payment_id');
  realign('rental',    'rental_id');
  realign('staff',     'staff_id');
  realign('store',     'store_id');
END;
