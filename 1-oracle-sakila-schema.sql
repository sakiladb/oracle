CREATE VIEW customer_list AS
SELECT cu.customer_id              AS id,
       cu.first_name || ' ' || cu.last_name AS name,
       a.address                   AS address,
       a.postal_code               AS zip_code,
       a.phone                     AS phone,
       city.city                   AS city,
       country.country             AS country,
       CASE WHEN cu.active = 1 THEN 'active' ELSE '' END AS notes,
       cu.store_id                 AS sid
FROM customer cu
JOIN address  a       ON cu.address_id = a.address_id
JOIN city             ON a.city_id     = city.city_id
JOIN country          ON city.country_id = country.country_id;

CREATE VIEW staff_list AS
SELECT s.staff_id                 AS id,
       s.first_name || ' ' || s.last_name AS name,
       a.address                  AS address,
       a.postal_code              AS zip_code,
       a.phone                    AS phone,
       city.city                  AS city,
       country.country            AS country,
       s.store_id                 AS sid
FROM staff s
JOIN address  a   ON s.address_id  = a.address_id
JOIN city         ON a.city_id     = city.city_id
JOIN country      ON city.country_id = country.country_id;

CREATE VIEW sales_by_store AS
SELECT c.city || ',' || cy.country         AS store,
       m.first_name || ' ' || m.last_name  AS manager,
       SUM(p.amount)                        AS total_sales
FROM payment   p
JOIN rental    r  ON p.rental_id    = r.rental_id
JOIN inventory i  ON r.inventory_id = i.inventory_id
JOIN store     s  ON i.store_id     = s.store_id
JOIN address   a  ON s.address_id   = a.address_id
JOIN city      c  ON a.city_id      = c.city_id
JOIN country   cy ON c.country_id   = cy.country_id
JOIN staff     m  ON s.manager_staff_id = m.staff_id
GROUP BY s.store_id, c.city, cy.country, m.first_name, m.last_name
ORDER BY cy.country, c.city;

CREATE VIEW sales_by_film_category AS
SELECT c.name         AS category,
       SUM(p.amount)  AS total_sales
FROM payment       p
JOIN rental        r  ON p.rental_id    = r.rental_id
JOIN inventory     i  ON r.inventory_id = i.inventory_id
JOIN film          f  ON i.film_id      = f.film_id
JOIN film_category fc ON f.film_id      = fc.film_id
JOIN category      c  ON fc.category_id = c.category_id
GROUP BY c.name
ORDER BY total_sales DESC;

CREATE VIEW film_list AS
SELECT film.film_id                AS fid,
       film.title                  AS title,
       film.description            AS description,
       category.name               AS category,
       film.rental_rate            AS price,
       film.length                 AS length,
       film.rating                 AS rating,
       LISTAGG(actor.first_name || ' ' || actor.last_name, ', ')
         WITHIN GROUP (ORDER BY actor.last_name, actor.first_name) AS actors
FROM category
LEFT JOIN film_category ON category.category_id = film_category.category_id
LEFT JOIN film          ON film_category.film_id = film.film_id
JOIN film_actor         ON film.film_id = film_actor.film_id
JOIN actor              ON film_actor.actor_id = actor.actor_id
GROUP BY film.film_id, film.title, film.description, category.name,
         film.rental_rate, film.length, film.rating;
