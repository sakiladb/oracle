CREATE TABLE actor (
  actor_id    NUMBER(5)     GENERATED BY DEFAULT AS IDENTITY,
  first_name  VARCHAR2(45)  NOT NULL,
  last_name   VARCHAR2(45)  NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_actor PRIMARY KEY (actor_id)
);

CREATE TABLE country (
  country_id  NUMBER(5)     GENERATED BY DEFAULT AS IDENTITY,
  country     VARCHAR2(50)  NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_country PRIMARY KEY (country_id)
);

CREATE TABLE city (
  city_id     NUMBER(5)     GENERATED BY DEFAULT AS IDENTITY,
  city        VARCHAR2(50)  NOT NULL,
  country_id  NUMBER(5)     NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_city PRIMARY KEY (city_id)
);

CREATE TABLE address (
  address_id  NUMBER(5)     GENERATED BY DEFAULT AS IDENTITY,
  address     VARCHAR2(50)  NOT NULL,
  address2    VARCHAR2(50)  DEFAULT NULL,
  -- Oracle treats '' as NULL, and Sakila address data has rows with empty
  -- district/phone strings, so NOT NULL would reject them.
  district    VARCHAR2(20)  DEFAULT NULL,
  city_id     NUMBER(5)     NOT NULL,
  postal_code VARCHAR2(10)  DEFAULT NULL,
  phone       VARCHAR2(20)  DEFAULT NULL,
  -- location GEOMETRY column omitted.
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_address PRIMARY KEY (address_id)
);

CREATE TABLE category (
  category_id NUMBER(3)     GENERATED BY DEFAULT AS IDENTITY,
  name        VARCHAR2(25)  NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_category PRIMARY KEY (category_id)
);

CREATE TABLE language (
  language_id NUMBER(3)     GENERATED BY DEFAULT AS IDENTITY,
  name        CHAR(20)      NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_language PRIMARY KEY (language_id)
);

CREATE TABLE film (
  film_id              NUMBER(5)      GENERATED BY DEFAULT AS IDENTITY,
  title                VARCHAR2(128)  NOT NULL,
  -- VARCHAR2 (not CLOB) so views can GROUP BY description; Sakila data is well
  -- within Oracle's default 4000-byte VARCHAR2 limit.
  description          VARCHAR2(4000) DEFAULT NULL,
  release_year         NUMBER(4)      DEFAULT NULL,
  language_id          NUMBER(3)      NOT NULL,
  original_language_id NUMBER(3)      DEFAULT NULL,
  rental_duration      NUMBER(3)      DEFAULT 3 NOT NULL,
  rental_rate          NUMBER(4,2)    DEFAULT 4.99 NOT NULL,
  length               NUMBER(5)      DEFAULT NULL,
  replacement_cost     NUMBER(5,2)    DEFAULT 19.99 NOT NULL,
  rating               VARCHAR2(10)   DEFAULT 'G',
  -- special_features stored as comma-separated VARCHAR2 (no MySQL SET equivalent).
  special_features     VARCHAR2(100)  DEFAULT NULL,
  last_update          TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_film PRIMARY KEY (film_id),
  CONSTRAINT chk_film_rating CHECK (rating IN ('G','PG','PG-13','R','NC-17'))
);

CREATE TABLE film_actor (
  actor_id    NUMBER(5)  NOT NULL,
  film_id     NUMBER(5)  NOT NULL,
  last_update TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_film_actor PRIMARY KEY (actor_id, film_id)
);

CREATE TABLE film_category (
  film_id     NUMBER(5)  NOT NULL,
  category_id NUMBER(3)  NOT NULL,
  last_update TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_film_category PRIMARY KEY (film_id, category_id)
);

CREATE TABLE inventory (
  inventory_id NUMBER(8)  GENERATED BY DEFAULT AS IDENTITY,
  film_id      NUMBER(5)  NOT NULL,
  store_id     NUMBER(3)  NOT NULL,
  last_update  TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_inventory PRIMARY KEY (inventory_id)
);

-- staff and store form a cycle (store.manager_staff_id -> staff,
-- staff.store_id -> store). Create both tables first, add FKs at the end.
CREATE TABLE staff (
  staff_id    NUMBER(3)     GENERATED BY DEFAULT AS IDENTITY,
  first_name  VARCHAR2(45)  NOT NULL,
  last_name   VARCHAR2(45)  NOT NULL,
  address_id  NUMBER(5)     NOT NULL,
  -- picture BLOB column omitted.
  email       VARCHAR2(50)  DEFAULT NULL,
  store_id    NUMBER(3)     NOT NULL,
  active      NUMBER(1)     DEFAULT 1 NOT NULL,
  username    VARCHAR2(16)  NOT NULL,
  password    VARCHAR2(40)  DEFAULT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_staff PRIMARY KEY (staff_id),
  CONSTRAINT chk_staff_active CHECK (active IN (0, 1))
);

CREATE TABLE store (
  store_id         NUMBER(3)  GENERATED BY DEFAULT AS IDENTITY,
  manager_staff_id NUMBER(3)  NOT NULL,
  address_id       NUMBER(5)  NOT NULL,
  last_update      TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_store PRIMARY KEY (store_id),
  CONSTRAINT uq_store_manager UNIQUE (manager_staff_id)
);

CREATE TABLE customer (
  customer_id NUMBER(5)     GENERATED BY DEFAULT AS IDENTITY,
  store_id    NUMBER(3)     NOT NULL,
  first_name  VARCHAR2(45)  NOT NULL,
  last_name   VARCHAR2(45)  NOT NULL,
  email       VARCHAR2(50)  DEFAULT NULL,
  address_id  NUMBER(5)     NOT NULL,
  active      NUMBER(1)     DEFAULT 1 NOT NULL,
  create_date TIMESTAMP     NOT NULL,
  last_update TIMESTAMP     DEFAULT SYSTIMESTAMP,
  CONSTRAINT pk_customer PRIMARY KEY (customer_id),
  CONSTRAINT chk_customer_active CHECK (active IN (0, 1))
);

CREATE TABLE rental (
  rental_id    NUMBER(10)  GENERATED BY DEFAULT AS IDENTITY,
  rental_date  TIMESTAMP   NOT NULL,
  inventory_id NUMBER(8)   NOT NULL,
  customer_id  NUMBER(5)   NOT NULL,
  return_date  TIMESTAMP   DEFAULT NULL,
  staff_id     NUMBER(3)   NOT NULL,
  last_update  TIMESTAMP   DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT pk_rental PRIMARY KEY (rental_id),
  CONSTRAINT uq_rental_unique UNIQUE (rental_date, inventory_id, customer_id)
);

CREATE TABLE payment (
  payment_id   NUMBER(5)    GENERATED BY DEFAULT AS IDENTITY,
  customer_id  NUMBER(5)    NOT NULL,
  staff_id     NUMBER(3)    NOT NULL,
  rental_id    NUMBER(10)   DEFAULT NULL,
  amount       NUMBER(5,2)  NOT NULL,
  payment_date TIMESTAMP    NOT NULL,
  last_update  TIMESTAMP    DEFAULT SYSTIMESTAMP,
  CONSTRAINT pk_payment PRIMARY KEY (payment_id)
);

